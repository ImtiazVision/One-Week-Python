# Boolean is one of the 4 basic data type in python used to make decisions, compare things, and logical operations. Every boolean is either True or False.
#For example, has the user logged in? Yes or No. If they have logged in, then the boolean is True. If they have not logged in, then the boolean is False. If the user is not logged in, ask them to log in before liking a post or commenting on a post. If the user is logged in and if they already have liked the post, clicking  on the post would lead to unliking the post. 

# Comparison Operators:

# a > b : Truthy if a is greater than b
# a < b : Truthy if a is less than b
# a >= b : Truthy if a is greater than or equal to b
# a <= b : Truthy if a is less than or equal to b
# a == b : Truthy if a is equal to b
# a != b : Truthy if a is not equal to b

# Identity Operators:

# is : Evaluates to True if a and b both refer to the same object in memory (i.e. same memory address)

# is not : Evaluates to True if a and b do NOT refer to the same object in memory (i.e. different memory address)

# Falsey Values 

# False, 0, 0.0, None, range(0), empty strings "", Empty data structures (), [], {}, set() etc.

# 'in' operator looks for the presence of an item in a list, tuple, string, dictionary, set, or any other iterable. It looks to see if an item is a member of a sequence. If the item is not found, it returns False. If the item is found, it returns True.