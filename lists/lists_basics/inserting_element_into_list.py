# insert(index, element)
# index before which to insert the element
# element to to inserted into the list 

nums = [8, 89, 22]
nums.insert(1, 'hello') # insert 'hello' before the element currently located at index 1 i.e. '89' 
print(nums)  # [8, 'hello', 89, 22]

# Typically, 'insert()' is used to enter element in the middle of a list or at the beginning of a list
