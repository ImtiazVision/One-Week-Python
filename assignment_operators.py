# initialize the variable prize_money to be $800
prize_money = 800
print("prize_money", prize_money)
# adding $80 to the prize_money variable to update it
prize_money = prize_money + 80  # this yields 880
print("prize_money", prize_money)

# alternatively we can do the same thing using the += operator
prize_money += 80  # this yields 960
print("prize_money", prize_money)

# Similarly we can do the opposite thing using the -= operator
prize_money -= 55 # this yields 905
print("prize_money", prize_money)

# Similarly, we can do the multiplication  using the *= operator
prize_money *= 100 # this yields 90500
print("Prize_money", prize_money)

# Similarly, we can do the division using the /= operator
prize_money /= 100 # this yields 905.00
print("Prize_money", prize_money)