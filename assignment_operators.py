# initialize the variable prize_money to be $800
prize_money = 800
print("prize_money", prize_money)
# adding $80 to the prize_money variable to update it
prize_money = prize_money + 80  # this yields 880
print("prize_money", prize_money)

# alternatively we can do the same thing using the += operator
prize_money += 80  # this yields 960
print("prize_money", prize_money)


